JQuery('#Membro1').click(() => {
    if (JQuery('#CMembro1').attr('active') === 'false') {
        JQuery('#CMembro1').attr('active', 'true')
        return JQuery('#CMembro1').css('display', 'block')
    }
})
JQuery('#CMembro1').click(() => {
    if (JQuery('#CMembro1').attr('active') === 'true') {
        JQuery('#CMembro1').attr('active', 'false')
        return JQuery('#CMembro1').css('display', 'none')
    }
})
