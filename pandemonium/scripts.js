$('#Membro1').click(() => {
    if ($('#CMembro1').attr('active') === 'false') {
        $('#CMembro1').attr('active', 'true')
        return $('#CMembro1').css('display', 'block')
    }
})
$('#CMembro1').click(() => {
    if ($('#CMembro1').attr('active') === 'true') {
        $('#CMembro1').attr('active', 'false')
        return $('#CMembro1').css('display', 'none')
    }
})
